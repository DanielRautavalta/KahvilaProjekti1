package kahvila;

public class Asiakas {
    private int raha;
    public int Asiakas(int raha) {
        this.raha = raha;
    }

    public int getRaha() {
        int raha = this.raha;
        return this.raha;
    }

    public void otaRaha(int maksu) {
        this.raha = this.raha - raha;
    }



}
